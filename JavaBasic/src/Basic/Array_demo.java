package Basic;


public class Array_demo {
	
	public static void main(String[] args) {
		
		int x = 3;
		int y = 5;  
		
		x+=y; 
		System.out.println(" x = "+x);
		
		x-=y; 
		System.out.println(" x = "+x);
		
		x*=y; 
		System.out.println(" x = "+x);
		
		x/=y;
		System.out.println(" x = "+x);
		
	}
	
	

}
